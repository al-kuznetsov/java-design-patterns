package command.simpleremote;

public class GarageDoor {

	public void up() {
		System.out.println("The door opens!");
	}

	public void down() {
		System.out.println("The door closes!");
	}

	public void stop() {
		System.out.println("The door stopped");
	}

	public void lightOn() {
		System.out.println("The door light is on");
	}

	public void lightOff() {
		System.out.println("The door light is off");
	}

}
